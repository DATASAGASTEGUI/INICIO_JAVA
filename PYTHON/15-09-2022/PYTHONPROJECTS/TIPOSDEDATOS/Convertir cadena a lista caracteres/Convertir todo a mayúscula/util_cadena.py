def convertir_cadena_a_lista_libreria(cadena):
    caracteres_l = list(cadena)
    return caracteres_l

def convertir_cadena_a_lista_algoritmo(cadena): 
    caracteres_l = []
    for x in cadena:
        caracteres_l.append(x)
    return caracteres_l

